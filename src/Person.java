public class Person {

    private String name;
    private int age;
    private String gender;
    private String state;
    private int year;
    private int nameCount;

    public Person(String name, int age, String gender, String state, int year, int nameCount) {
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.state = state;
        this.year = year;
        this.nameCount = nameCount;
    }

}
